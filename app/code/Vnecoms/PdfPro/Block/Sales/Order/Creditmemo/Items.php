namespace Vnecoms\PdfPro\Block\Sales\Order\Creditmemo;

class Items extends \Magento\Sales\Block\Order\Creditmemo\Items
{
    /**
     * @var \Vnecoms\PdfPro\Helper\Data
     */
    protected $helper;

    /**
     * Items constructor.
     *
     * @param \Vnecoms\PdfPro\Helper\Data                      $helper
     * @param \Magento\Framework\View\Element\Template\Context $context
     * @param \Magento\Framework\Registry                      $registry
     * @param array                                            $data
     */
    public function __construct(
        \Vnecoms\PdfPro\Helper\Data $helper,
        \Magento\Framework\View\Element\Template\Context $context,
        \Magento\Framework\Registry $registry,
        array $data
    ) {
        $this->helper = $helper;
        parent::__construct($context, $registry, $data);
    }

    protected function _toHtml()
    {
        $this->setModuleName($this->extractModuleName('Magento\Sales\Block\Order\Creditmemo\Items'));

        return parent::_toHtml();
    }

