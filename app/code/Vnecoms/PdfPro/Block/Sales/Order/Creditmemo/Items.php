    /**
     * @param object $creditmemo
     *
     * @return string
     */
    public function getPrintCreditmemoUrl($creditmemo)
    {
        if (!$this->helper->getConfig('pdfpro/general/enabled') || !$this->helper->getConfig('pdfpro/general/allow_customer_print')) {
            return parent::getPrintCreditmemoUrl($creditmemo);
        }

        return $this->getUrl('pdfpro/order/printCreditmemo', ['creditmemo_id' => $creditmemo->getId()]);
    }

    /**
     * @param object $order
     *
     * @return string
     */
    public function getPrintAllCreditmemosUrl($order)
    {
        return parent::getPrintAllCreditmemosUrl($order);
        if (!$this->helper->getConfig('pdfpro/general/enabled') || $this->helper->getConfig('pdfpro/general/allow_customer_print')) {
            return parent::getPrintAllCreditmemosUrl($order);
        }

        return $this->getUrl('pdfpro/order/printCreditmemo', ['order_id' => $order->getId()]);
    }
}
